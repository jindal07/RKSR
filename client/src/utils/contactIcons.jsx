function BrandIcon({ name, size = 22 }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="1.5"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      {BRAND_PATHS[name]}
    </svg>
  );
}

// Pick a fitting icon for a contact link from its URL
export function LinkIcon({ url = '', size = 15 }) {
  if (url.startsWith('mailto:')) return <Mail size={size} strokeWidth={1.5} />;
  if (url.startsWith('tel:')) return <Phone size={size} strokeWidth={1.5} />;
  for (const brand of Object.keys(BRAND_PATHS)) {
    if (url.includes(`${brand}.com`)) return <BrandIcon name={brand} size={size} />;
  }
  return <ExternalLink size={size} strokeWidth={1.5} />;
}
